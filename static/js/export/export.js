  function renderFrameToCtx(tc,fid,ow,oh){
    tc.fillStyle=S.pc; tc.fillRect(0,0,ow,oh);
    const scale = ow / S.CW; tc.save(); tc.scale(scale, scale);
    const c=S.fc.get(fid);
    if(c && c.P) {
      const tmp = document.createElement('canvas'); tmp.width = S.CW * C.DPR; tmp.height = S.CH * C.DPR;
      const tx = tmp.getContext('2d'); tx.putImageData(c.P, 0, 0); tc.drawImage(tmp, 0, 0, S.CW, S.CH);
    }
    S.layerOrder.forEach(layer => {
      if(c && c[layer]){
        const tmp = document.createElement('canvas'); tmp.width = S.CW * C.DPR; tmp.height = S.CH * C.DPR;
        const tx = tmp.getContext('2d'); tx.putImageData(c[layer], 0, 0); tc.drawImage(tmp, 0, 0, S.CW, S.CH);
      }
      if(VP) VP.renderAllPaths(tc, fid, layer, S.CW, S.CH);
    });
    tc.restore();
  }

  EX.exportSVG = () => { 
    const w = S.CW, h = S.CH;
    let svgContent = `<?xml version="1.0" encoding="UTF-8"?>\n<svg xmlns="http://www.w3.org/2000/svg" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}">\n  <title>UgokuDraw Frame ${S.cf+1}</title>\n  <desc>Generated by UgokuDraw v${G.VERSION}</desc>\n  <rect width="100%" height="100%" fill="${S.pc}"/>\n`;
    const fid = E.curId();
    S.layerOrder.forEach(layer => {
      const paths = VP.getFramePaths(fid, layer);
      if(!paths || paths.length === 0) return;
      svgContent += `  <g id="layer-${layer}" opacity="1">\n`;
      paths.forEach(p => {
        const pts = (p.smooth > 0 && p.pts.length >= 3) ? U.stabilize(p.pts, p.smooth) : p.pts;
        if(pts.length === 0) return;
        const color = p.color; const opacity = p.alpha; const strokeWidth = p.size;
        const lineCap = 'round'; const lineJoin = 'round'; let d = ''; let fill = 'none';
        if(p.type === 'pen' || p.type === 'eraser') {
          if(pts.length === 1) { d = `M ${pts[0].x} ${pts[0].y} L ${pts[0].x+0.1} ${pts[0].y}`; } 
          else {
            d = `M ${pts[0].x} ${pts[0].y}`;
            for(let i=1; i<pts.length-1; i++){
              const mx = (pts[i].x + pts[i+1].x) / 2; const my = (pts[i].y + pts[i+1].y) / 2;
              d += ` Q ${pts[i].x} ${pts[i].y} ${mx} ${my}`;
            }
            d += ` L ${pts[pts.length-1].x} ${pts[pts.length-1].y}`;
          }
        } else if (p.type === 'line') { d = `M ${pts[0].x} ${pts[0].y} L ${pts[pts.length-1].x} ${pts[pts.length-1].y}`;
        } else if (p.type === 'rect') { const w = pts[1].x - pts[0].x; const h = pts[1].y - pts[0].y; d = `M ${pts[0].x} ${pts[0].y} h ${w} v ${h} h ${-w} z`; if(p.fill) fill = color;
        } else if (p.type === 'circle') { const rx = Math.abs(pts[1].x - pts[0].x)/2; const ry = Math.abs(pts[1].y - pts[0].y)/2; const cx = (pts[0].x + pts[1].x)/2; const cy = (pts[0].y + pts[1].y)/2; d = `M ${cx+rx} ${cy} a ${rx} ${ry} 0 1 0 ${-2*rx} 0 a ${rx} ${ry} 0 1 0 ${2*rx} 0`; if(p.fill) fill = color;
        } else if (p.type === 'star' || p.type === 'heart') { d = `M ${pts[0].x} ${pts[0].y}`; for(let i=1; i<pts.length; i++) d += ` L ${pts[i].x} ${pts[i].y}`; if(p.type==='rect'||p.type==='circle'||p.type==='star'||p.type==='heart') d+=' Z'; if(p.fill) fill = color; }

        if(p.outline && p.type !== 'eraser') { const outWidth = strokeWidth + p.outlineWidth * 2; svgContent += `    <path d="${d}" stroke="${p.outlineColor}" stroke-width="${outWidth}" fill="${fill}" stroke-linecap="${lineCap}" stroke-linejoin="${lineJoin}" opacity="${opacity}" />\n`; }
        if(p.type !== 'eraser') { svgContent += `    <path d="${d}" stroke="${color}" stroke-width="${strokeWidth}" fill="${fill}" stroke-linecap="${lineCap}" stroke-linejoin="${lineJoin}" opacity="${opacity}" />\n`; }
      });
      svgContent += `  </g>\n`;
    });
    svgContent += `</svg>`;
    downloadBlob(new Blob([svgContent], {type: 'image/svg+xml;charset=utf-8'}), `ugoku_frame_${S.cf+1}.svg`);
    E.toast('ベクターSVG保存完了');
  };

  EX.exportGIF=async quality=>{E.snapToCache();for(let i=0;i<S.frames.length;i++)await E.ensureCached(S.frames[i].id);const mo=U.$('exportModal');mo.classList.add('show');U.$('expTitle').textContent='GIF生成中...';const sc=quality==='low'?0.5:quality==='mid'?0.75:1;const ow=Math.round(S.CW*sc),oh=Math.round(S.CH*sc);const delay=Math.round(1000/S.fps);const tmp=document.createElement('canvas');tmp.width=ow;tmp.height=oh;const tc=tmp.getContext('2d');const framesData=[];for(let i=0;i<S.frames.length;i++){renderFrameToCtx(tc,S.frames[i].id,ow,oh);framesData.push(tc.getImageData(0,0,ow,oh).data.buffer);U.$('expBar').style.width=((i+1)/S.frames.length*40)+'%';U.$('expMsg').textContent=`レンダリング ${i+1}/${S.frames.length}`;await new Promise(r=>setTimeout(r,2));}if(E.W){const res=await E.workerCall({type:'gifEncode',w:ow,h:oh,framesData,delay},framesData,d=>{U.$('expBar').style.width=(40+d.frame/d.total*55)+'%';U.$('expMsg').textContent=`エンコード ${d.frame}/${d.total}`;});U.$('expBar').style.width='100%';downloadBlob(new Blob([new Uint8Array(res.data)],{type:'image/gif'}),'ugoku_draw_'+Date.now()+'.gif');mo.classList.remove('show');E.toast('GIF保存完了');}else{mo.classList.remove('show');E.toast('Worker非対応');}};
  EX.exportPNGSequence=async quality=>{E.snapToCache();for(let i=0;i<S.frames.length;i++)await E.ensureCached(S.frames[i].id);const sc=quality==='low'?0.5:quality==='mid'?1:2;const ow=Math.round(S.CW*sc),oh=Math.round(S.CH*sc);const tmp=document.createElement('canvas');tmp.width=ow;tmp.height=oh;const tc=tmp.getContext('2d');const mo=U.$('exportModal');mo.classList.add('show');U.$('expTitle').textContent='PNG出力中...';for(let i=0;i<S.frames.length;i++){renderFrameToCtx(tc,S.frames[i].id,ow,oh);const blob=await new Promise(r=>tmp.toBlob(r,'image/png'));downloadBlob(blob,`frame_${String(i+1).padStart(4,'0')}.png`);U.$('expBar').style.width=((i+1)/S.frames.length*100)+'%';U.$('expMsg').textContent=`${i+1}/${S.frames.length}`;await new Promise(r=>setTimeout(r,50));}mo.classList.remove('show');E.toast('PNG連番出力完了');};
